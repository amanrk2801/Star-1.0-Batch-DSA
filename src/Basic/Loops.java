package Basic;

public class Loops {
    public static void main(String[] args) {
//        for loop
        for (int i = 0; i < 5; i++) {
//            System.out.println("I'm for loop");
        }

//        while loop
        int i = 0;
        while (i < 5) {
//            System.out.println("I'm while loop");
            i++;
        }

//        do While
        int j = 0;
        do {
            System.out.println("I'm do while loop");
            j++;
        } while (j < 5);

    }
}
